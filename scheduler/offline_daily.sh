#!/bin/bash
# ===================================================
# 每日离线数仓全流程调度
# 用途：按五层数据仓库架构顺序执行 T+1 离线计算
# 触发：DolphinScheduler 每日 01:00 自动触发
# 参数：$1 - 日期（可选，默认 T-1）
# ===================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PROJECT_ROOT="$(dirname "$SCRIPT_DIR")"

source ${PROJECT_ROOT}/script/common/env.sh
source ${PROJECT_ROOT}/script/common/date_util.sh

DO_DATE=$(get_do_date "$1")

echo "╔══════════════════════════════════════════════════════════════╗"
echo "║   GMall 离线数仓 — 每日调度                                  ║"
echo "║   日期: ${DO_DATE}                                           ║"
echo "║   开始时间: $(date '+%Y-%m-%d %H:%M:%S')                     ║"
echo "╚══════════════════════════════════════════════════════════════╝"

# ============================
# 第 1 步：ODS 层数据装载
# ============================
echo ""
echo "═══════════════════════════════════════════════"
echo "  [1/6] ODS 层 — 贴源层数据装载"
echo "═══════════════════════════════════════════════"
bash ${PROJECT_ROOT}/script/ods/ods_load.sh ${DO_DATE}
echo "[OK] ODS 层装载完成"

# ============================
# 第 2 步：DIM 层维度计算
# ============================
echo ""
echo "═══════════════════════════════════════════════"
echo "  [2/6] DIM 层 — 维度层计算"
echo "═══════════════════════════════════════════════"
bash ${PROJECT_ROOT}/script/dim/ods_to_dim.sh ${DO_DATE}
echo "[OK] DIM 层计算完成"

# ============================
# 第 3 步：DWD 层明细计算
# ============================
echo ""
echo "═══════════════════════════════════════════════"
echo "  [3/6] DWD 层 — 明细层 ETL"
echo "═══════════════════════════════════════════════"
bash ${PROJECT_ROOT}/script/dwd/ods_to_dwd.sh ${DO_DATE}
echo "[OK] DWD 层计算完成"

# ============================
# 第 4 步：DWS 层汇总计算
# ============================
echo ""
echo "═══════════════════════════════════════════════"
echo "  [4/6] DWS 层 — 汇总层计算"
echo "═══════════════════════════════════════════════"

echo "    [4.1] DWS 1d 日汇总..."
bash ${PROJECT_ROOT}/script/dws/dwd_to_dws_1d.sh ${DO_DATE}
echo "    [OK]"

echo "    [4.2] DWS nd N日汇总..."
bash ${PROJECT_ROOT}/script/dws/dws_1d_to_dws_nd.sh ${DO_DATE}
echo "    [OK]"

echo "    [4.3] DWS td 累计汇总..."
bash ${PROJECT_ROOT}/script/dws/dws_1d_to_dws_td.sh ${DO_DATE}
echo "    [OK]"

echo "[OK] DWS 层计算完成"

# ============================
# 第 5 步：ADS 层应用计算
# ============================
echo ""
echo "═══════════════════════════════════════════════"
echo "  [5/6] ADS 层 — 应用层报表计算"
echo "═══════════════════════════════════════════════"
bash ${PROJECT_ROOT}/script/ads/dws_to_ads.sh ${DO_DATE}
echo "[OK] ADS 层计算完成"

# ============================
# 第 6 步：ADS 数据导出到 MySQL
# ============================
echo ""
echo "═══════════════════════════════════════════════"
echo "  [6/6] 数据导出 — ADS → MySQL"
echo "═══════════════════════════════════════════════"
bash ${PROJECT_ROOT}/script/export/hdfs_to_mysql.sh ${DO_DATE}
echo "[OK] 数据导出完成"

# ============================
# 完成
# ============================
echo ""
echo "╔══════════════════════════════════════════════════════════════╗"
echo "║   全流程调度完成!                                            ║"
echo "║   日期: ${DO_DATE}                                           ║"
echo "║   结束时间: $(date '+%Y-%m-%d %H:%M:%S')                     ║"
echo "╚══════════════════════════════════════════════════════════════╝"
