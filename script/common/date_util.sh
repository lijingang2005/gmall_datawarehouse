#!/bin/bash
# ===================================================
# 日期工具函数
# 用途：封装日期计算逻辑
# ===================================================

# -------------------------------------------------
# 获取业务日期（默认 T-1）
# 参数: $1 - 可选指定日期
# -------------------------------------------------
get_do_date() {
    if [ -n "$1" ]; then
        echo "$1"
    else
        date -d "-1 day" +%F
    fi
}

# -------------------------------------------------
# 获取 N 天前的日期
# 参数: $1 - 基准日期 (yyyy-MM-dd)
#       $2 - N 天
# -------------------------------------------------
get_before_date() {
    local base_date="$1"
    local days="$2"
    date -d "${base_date} -${days} days" +%F
}

# -------------------------------------------------
# 获取 N 天后的日期
# 参数: $1 - 基准日期 (yyyy-MM-dd)
#       $2 - N 天
# -------------------------------------------------
get_after_date() {
    local base_date="$1"
    local days="$2"
    date -d "${base_date} +${days} days" +%F
}

# -------------------------------------------------
# 判断是否为周一（周调度用）
# 参数: $1 - 日期 (yyyy-MM-dd)
# -------------------------------------------------
is_monday() {
    local day_of_week=$(date -d "$1" +%u)
    [ "$day_of_week" -eq 1 ]
}

# -------------------------------------------------
# 判断是否为月初第一天（月调度用）
# 参数: $1 - 日期 (yyyy-MM-dd)
# -------------------------------------------------
is_first_day_of_month() {
    local day_of_month=$(date -d "$1" +%d)
    [ "$day_of_month" = "01" ]
}
