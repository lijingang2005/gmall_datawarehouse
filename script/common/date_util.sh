#!/bin/bash
# ===================================================
# 日期工具函数
# 用途：封装日期计算逻辑
# ===================================================

# -------------------------------------------------
# 获取业务日期（默认 T-1）
# 参数: $1 - 可选指定日期
# -------------------------------------------------
get_do_date() {
    if [ -n "$1" ]; then
        echo "$1"
    else
        date -d "-1 day" +%F
    fi
}

# -------------------------------------------------
# 获取 N 天前的日期
# 参数: $1 - 基准日期 (yyyy-MM-dd)
#       $2 - N 天
# -------------------------------------------------
get_before_date() {
    local base_date="$1"
    local days="$2"
    date -d "${base_date} -${days} days" +%F
}

# -------------------------------------------------
# 获取 N 天后的日期
# 参数: $1 - 基准日期 (yyyy-MM-dd)
#       $2 - N 天
# -------------------------------------------------
get_after_date() {
    local base_date="$1"
    local days="$2"
    date -d "${base_date} +${days} days" +%F
}
